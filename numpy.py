import numpy as np

x = 1.0 #define a float
y = 2.0	#define another float

#trigonometry
print(np.sin(x))
print(np.cos(x))
print(np.tan(x))
print(np.arcsin(x))
print(np.arccos(x))
print(np.arctan(x))
print(np.arctan2(x))	#arctan(x/y)
print(np.rad2deg(x))	#convert rad to deg

#hyperbolic functions
print(np.sinh(x))
print(np.cosh(x))
print(np.tanh(x))
print(np.arcsinh(x))
print(np.arccosh(x))
print(np.arctanh(x))